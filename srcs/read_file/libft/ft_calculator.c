int		ft_calculator(int a, int b, char c)
{
	int d;

	d = 1;
	if (c == '+')
		d = a + b;
	if (c == '-')
		d = a - b;
	if (c == '/')
		d = a / b;
	if (c == '*')
		d = a * b;
	if (c == '^')
	{
		while (b > 0)
		{
			d = d * a;
			b--;
		}
	}
	return (d);
}
