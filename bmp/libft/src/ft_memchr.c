#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*bstr;
	size_t			i;

	bstr = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (bstr[i] == (unsigned char)c)
			return (&bstr[i]);
		i++;
	}
	return (NULL);
}
