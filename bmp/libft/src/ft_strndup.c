#include "libft.h"
#include <stdlib.h>

char	*ft_strndup(const char *s1, size_t n)
{
	size_t		i;
	char		*copy;

	copy = (char*)malloc(sizeof(char) * n + 1);
	if (!copy)
		return (NULL);
	i = 0;
	while (s1[i] && i < n)
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
